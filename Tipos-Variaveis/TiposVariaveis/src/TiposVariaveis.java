public class TiposVariaveis {
    public static void main(String[] args) throws Exception {
        //tipos primitivos

        boolean verdadeiro; //Só admite valores true ou false
        char caractere; //Usa código UNICODE e ocupa cada caractere 16bits
        String texto; //Armazenar textos(numeros ou caracteres)
        
        //Numeros inteiros
        byte numero1; //1 byte
        short numero2; //2 bytes
        int numero3; //4 bytes 
        long numero4; //8 bytes

        //Numeros reais
        float real; //4 bytes
        double real1; //8 bytes
    }
}
